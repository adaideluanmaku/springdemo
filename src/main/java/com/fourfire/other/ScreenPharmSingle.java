package com.fourfire.other;

public class ScreenPharmSingle {
	private Integer type;
	private Integer tempid;
	private String caseid;
	private Long mhiscode;
	private String userid;
	private String username;
	private String prescno;
	private String drugcodelist;
	private Integer taskid;

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getTempid() {
		return tempid;
	}

	public void setTempid(Integer tempid) {
		this.tempid = tempid;
	}

	public String getCaseid() {
		return caseid;
	}

	public void setCaseid(String caseid) {
		this.caseid = caseid;
	}

	public Long getMhiscode() {
		return mhiscode;
	}

	public void setMhiscode(Long mhiscode) {
		this.mhiscode = mhiscode;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPrescno() {
		return prescno;
	}

	public void setPrescno(String prescno) {
		this.prescno = prescno;
	}

	public String getDrugcodelist() {
		return drugcodelist;
	}

	public void setDrugcodelist(String drugcodelist) {
		this.drugcodelist = drugcodelist;
	}

	public Integer getTaskid() {
		return taskid;
	}

	public void setTaskid(Integer taskid) {
		this.taskid = taskid;
	}

}
